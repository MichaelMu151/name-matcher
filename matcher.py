_PUNCT_RE = re.compile(r"[\s\u3000（）()【】\[\]:：·,，、\.。;；\"'“”‘’\-—]")


def normalize_text(s) -> str:
    """全角/半角统一 + 去除常见标点空白，避免无意义差异干扰相似度计算"""
    if pd.isna(s):
        return ""
    s = unicodedata.normalize("NFKC", str(s))
    return _PUNCT_RE.sub("", s).strip()


def map_level(dtype_t: str) -> str:
    key = str(dtype_t).strip()
    lvl = LEVEL_MAP.get(key)
    if lvl is None:
        raise MatcherError(
            f"未知的 dtype_t: '{dtype_t}'，应为 省/地级市/县/区/县级市/经济区/功能区 之一"
        )
    return lvl


def confidence_tier(score: float) -> str:
    if score >= CFG.high_conf_thresh:
        return "高"
    elif score >= CFG.mid_conf_thresh:
        return "中"
    return "低"


def _lcs_length(a: str, b: str) -> int:
    n, m = len(a), len(b)
    if n == 0 or m == 0:
        return 0
    prev = [0] * (m + 1)
    for i in range(1, n + 1):
        cur = [0] * (m + 1)
        ai = a[i - 1]
        for j in range(1, m + 1):
            cur[j] = prev[j - 1] + 1 if ai == b[j - 1] else max(prev[j], cur[j - 1])
        prev = cur
    return prev[m]


def lcs_ratio(a: str, b: str) -> float:
    """最长公共子序列比例：专门捕捉'发展和改革委员会→发改委'这类逐字提取式缩写"""
    if not a or not b:
        return 0.0
    return _lcs_length(a, b) / max(len(a), len(b))


def char_dice(a: str, b: str) -> float:
    def bigrams(s):
        return {s[i:i + 2] for i in range(len(s) - 1)} if len(s) > 1 else {s}
    A, B = bigrams(a), bigrams(b)
    if not A or not B:
        return 0.0
    return 2 * len(A & B) / (len(A) + len(B))


def extract_features(raw_n: str, cand_n: str, tfidf_cos: float) -> np.ndarray:
    exact = 1.0 if (raw_n == cand_n and raw_n != "") else 0.0
    feats = {
        "ratio": fuzz.ratio(raw_n, cand_n) / 100.0,
        "partial_ratio": fuzz.partial_ratio(raw_n, cand_n) / 100.0,
        "token_sort_ratio": fuzz.token_sort_ratio(raw_n, cand_n) / 100.0,
        "jaro_winkler": JaroWinkler.normalized_similarity(raw_n, cand_n),
        "dice": char_dice(raw_n, cand_n),
        "lcs_ratio": lcs_ratio(raw_n, cand_n),
        "tfidf_cos": float(tfidf_cos),
        "len_ratio": min(len(raw_n), len(cand_n)) / max(len(raw_n), len(cand_n), 1),
        "exact": exact,
    }
    return np.array([feats[k] for k in FEATURE_NAMES], dtype=float)


def heuristic_score(feat_vec: np.ndarray) -> float:
    d = dict(zip(FEATURE_NAMES, feat_vec))
    if d["exact"] == 1.0:
        return 1.0
    return float(sum(d[k] * HEURISTIC_WEIGHTS[k] for k in FEATURE_NAMES))


