class CodeBook:
    REQUIRED_COLS = ["dept_id", "level", "standard_name", "aliases"]

    def __init__(self, path: str, backup_dir: str = "codebook_backups"):
        if not os.path.exists(path):
            raise CodeBookError(f"找不到 codebook 文件: {path}")
        self.path = path
        self.backup_dir = backup_dir
        os.makedirs(backup_dir, exist_ok=True)
        self.df = pd.read_excel(path, dtype=str).fillna("")
        self._validate()
        self.vectorizer: Optional[TfidfVectorizer] = None
        self.tfidf_matrix = None
        self.expanded: pd.DataFrame = pd.DataFrame()
        self._build_index()

    def _validate(self):
        missing = set(self.REQUIRED_COLS) - set(self.df.columns)
        if missing:
            raise CodeBookError(f"codebook 缺少必要列: {missing}")
        empty_names = self.df[self.df["standard_name"].str.strip() == ""]
        if len(empty_names) > 0:
            logger.warning(f"codebook 中有 {len(empty_names)} 行 standard_name 为空")
        dup = self.df.duplicated(subset=["dept_id", "level"], keep=False)
        if dup.any():
            logger.warning(f"codebook 中存在 {dup.sum()} 行 (dept_id, level) 重复，建议核查")

    def _build_index(self):
        self.df["norm_name"] = self.df["standard_name"].apply(normalize_text)
        rows = []
        for _, r in self.df.iterrows():
            variants = {r["norm_name"]}
            variants |= {normalize_text(a) for a in r["aliases"].split("|") if a.strip()}
            for v in variants:
                if v:
                    rows.append({"dept_id": r["dept_id"], "level": r["level"],
                                 "standard_name": r["standard_name"], "variant": v})
        self.expanded = pd.DataFrame(rows)
        if self.expanded.empty:
            raise CodeBookError("codebook 展开后为空，请检查 standard_name/aliases 是否填写")
        self.vectorizer = TfidfVectorizer(analyzer="char", ngram_range=(1, 2))
        self.tfidf_matrix = self.vectorizer.fit_transform(self.expanded["variant"].tolist())

    # ---------- 修改操作 ----------
    def add_alias(self, dept_id: str, level: str, raw_name_norm: str) -> bool:
        mask = (self.df["dept_id"] == dept_id) & (self.df["level"] == level)
        if not mask.any():
            logger.error(f"add_alias 失败: 找不到 dept_id={dept_id}, level={level}")
            return False
        idx = self.df[mask].index[0]
        alias_set = {a for a in self.df.at[idx, "aliases"].split("|") if a.strip()}
        norm_name = self.df.at[idx, "norm_name"]
        if raw_name_norm in alias_set or raw_name_norm == norm_name:
            return False
        alias_set.add(raw_name_norm)
        self.df.at[idx, "aliases"] = "|".join(sorted(alias_set))
        self._build_index()
        logger.info(f"新增别名: dept_id={dept_id}, level={level}, alias='{raw_name_norm}'")
        return True

    def add_department(self, dept_id: str, level: str, standard_name: str, aliases: str = ""):
        if ((self.df["dept_id"] == dept_id) & (self.df["level"] == level)).any():
            raise CodeBookError(f"dept_id={dept_id}, level={level} 已存在，不能重复添加")
        new_row = pd.DataFrame([{"dept_id": dept_id, "level": level,
                                  "standard_name": standard_name, "aliases": aliases}])
        self.df = pd.concat([self.df, new_row], ignore_index=True)
        self._build_index()
        logger.info(f"新增部门: dept_id={dept_id}, level={level}, standard_name={standard_name}")

    def save(self):
        if os.path.exists(self.path):
            ts = datetime.now().strftime("%Y%m%d_%H%M%S")
            backup_path = os.path.join(self.backup_dir, f"codebook_backup_{ts}.xlsx")
            try:
                shutil.copy2(self.path, backup_path)
                self._prune_backups()
            except Exception as e:
                logger.warning(f"备份 codebook 失败: {e}")
        tmp_path = self.path + ".tmp"
        self.df[self.REQUIRED_COLS].to_excel(tmp_path, index=False)
        os.replace(tmp_path, self.path)
        logger.info("codebook 已保存")

    def _prune_backups(self, keep: int = 20):
        files = sorted(
            (os.path.join(self.backup_dir, f) for f in os.listdir(self.backup_dir)),
            key=os.path.getmtime, reverse=True,
        )
        for f in files[keep:]:
            try:
                os.remove(f)
            except OSError:
                pass

    # ---------- 查询/浏览 ----------
    def search(self, level: Optional[str] = None, keyword: str = "") -> pd.DataFrame:
        df = self.df
        if level and level != "全部":
            df = df[df["level"] == level]
        if keyword:
            kw = normalize_text(keyword)
            df = df[df["norm_name"].str.contains(kw, na=False) |
                    df["aliases"].apply(lambda a: kw in normalize_text(a))]
        return df[self.REQUIRED_COLS]

    @property
    def stats(self) -> Dict[str, int]:
        return {
            "部门概念数": self.df["dept_id"].nunique(),
            "记录总行数": len(self.df),
            "变体总数(含别名)": len(self.expanded),
        }


