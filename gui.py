    # ================= 单条查询 =================
    def _build_query_tab(self):
        f = self.tab_query
        top = ttk.Frame(f); top.pack(fill="x", padx=10, pady=10)

        ttk.Label(top, text="原始部门名 w_name_o：").grid(row=0, column=0, sticky="e", padx=4, pady=4)
        self.entry_raw = ttk.Entry(top, width=40)
        self.entry_raw.grid(row=0, column=1, padx=4, pady=4)
        self.entry_raw.bind("<Return>", lambda e: self.on_query())

        ttk.Label(top, text="层级 dtype_t：").grid(row=1, column=0, sticky="e", padx=4, pady=4)
        self.combo_level = ttk.Combobox(top, values=DTYPE_OPTIONS, width=37, state="readonly")
        self.combo_level.grid(row=1, column=1, padx=4, pady=4)
        self.combo_level.current(0)

        ttk.Button(top, text="查询 (Enter)", command=self.on_query).grid(row=0, column=2, rowspan=2, padx=10)

        cols = ("rank", "standard_name", "score", "confidence", "dept_id")
        headers = ("排名", "候选标准名", "相似度得分", "置信度", "dept_id")
        self.tree = ttk.Treeview(f, columns=cols, show="headings", height=6)
        for c, h, w in zip(cols, headers, (50, 320, 100, 80, 140)):
            self.tree.heading(c, text=h)
            self.tree.column(c, width=w, anchor="center")
        self.tree.tag_configure("conf_high", background="#d9f2d9")
        self.tree.tag_configure("conf_mid", background="#fff3cd")
        self.tree.tag_configure("conf_low", background="#fadbd8")
        self.tree.pack(fill="x", padx=10, pady=6)

        btn_frame = ttk.Frame(f); btn_frame.pack(fill="x", padx=10, pady=4)
        ttk.Button(btn_frame, text="✔ 确认所选结果为正确答案", command=self.on_confirm).pack(side="left", padx=4)
        ttk.Button(btn_frame, text="✘ 均不匹配，标记待人工新增", command=self.on_flag_unmatched).pack(side="left", padx=4)

        ttk.Label(f, text="本次会话操作记录：").pack(anchor="w", padx=10, pady=(10, 0))
        self.session_log = scrolledtext.ScrolledText(f, height=10, state="disabled")
        self.session_log.pack(fill="both", expand=True, padx=10, pady=6)

    def _log_session(self, text: str):
        self.session_log.configure(state="normal")
        self.session_log.insert("end", f"[{datetime.now().strftime('%H:%M:%S')}] {text}\n")
        self.session_log.see("end")
        self.session_log.configure(state="disabled")

    def on_query(self):
        raw, dtype_t = self.entry_raw.get().strip(), self.combo_level.get()
        if not raw:
            messagebox.showwarning("提示", "请输入原始部门名称")
            return
        try:
            res = self.matcher.query(raw, dtype_t, topk=3)
        except MatcherError as e:
            messagebox.showerror("查询失败", str(e))
            return
        for i in self.tree.get_children():
            self.tree.delete(i)
        for _, r in res.iterrows():
            tag = CONF_TAGS.get(r["confidence"], "")
            self.tree.insert("", "end",
                              values=(r["rank"], r["standard_name"], r["score"], r["confidence"], r["dept_id"]),
                              tags=(tag,))
        self._log_session(f"查询 '{raw}'({dtype_t}) -> 返回{len(res)}个候选")

    def on_confirm(self):
        sel = self.tree.selection()
        if not sel:
            messagebox.showwarning("提示", "请先在结果列表中选中正确的一项")
            return
        vals = self.tree.item(sel[0], "values")
        dept_id, standard_name = vals[4], vals[1]
        raw, dtype_t = self.entry_raw.get().strip(), self.combo_level.get()
        try:
            self.matcher.confirm(raw, dtype_t, dept_id)
        except MatcherError as e:
            messagebox.showerror("确认失败", str(e))
            return
        self._log_session(f"确认 '{raw}' -> {standard_name}({dept_id})，已写入别名库")
        self._refresh_status()
        self.entry_raw.delete(0, "end")
        for i in self.tree.get_children():
            self.tree.delete(i)

    def on_flag_unmatched(self):
        raw, dtype_t = self.entry_raw.get().strip(), self.combo_level.get()
        if not raw:
            messagebox.showwarning("提示", "请输入原始部门名称")
            return
        self.matcher.flag_unmatched(raw, dtype_t)
        self._log_session(f"标记 '{raw}'({dtype_t}) 为无匹配，已记入 unmatched_log.xlsx 待人工新增")

    # ================= 批量处理 =================
    def _build_batch_tab(self):
        f = self.tab_batch
        top = ttk.Frame(f); top.pack(fill="x", padx=10, pady=10)

