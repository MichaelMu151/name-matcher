        self.run_batch_btn.pack(pady=10)

        self.batch_progress = ttk.Progressbar(f, orient="horizontal", length=500, mode="determinate")
        self.batch_progress.pack(pady=4)
        self.batch_status = ttk.Label(f, text="")
        self.batch_status.pack(pady=4)

