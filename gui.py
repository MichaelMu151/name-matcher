    def choose_batch_file(self):
        path = filedialog.askopenfilename(filetypes=[("Excel files", "*.xlsx")])
        if not path:
            return
        try:
            df = pd.read_excel(path, dtype=str)
        except Exception as e:
            messagebox.showerror("读取失败", str(e))
            return
        if not {"w_name_o", "dtype_t"}.issubset(df.columns):
            messagebox.showerror("格式错误", "文件必须包含 w_name_o 和 dtype_t 两列")
            return
        self.batch_path.set(path)
        self.batch_preview.config(text=f"共 {len(df)} 行数据，列: {list(df.columns)}")
        self._batch_df = df

    def run_batch(self):
        if not getattr(self, "_batch_df", None) is not None:
            pass
        if not hasattr(self, "_batch_df"):
            messagebox.showwarning("提示", "请先选择文件")
            return
        self.run_batch_btn.config(state="disabled")
        self.batch_progress["value"] = 0
        self.batch_status.config(text="处理中...")

        def worker():
            try:
                def cb(done, total):
                    self._batch_queue.put(("progress", done, total))
                result = self.matcher.batch_query(self._batch_df, topk=self.topk_var.get(),
                                                   progress_callback=cb)
                self._batch_queue.put(("done", result))
            except Exception as e:
                self._batch_queue.put(("error", str(e)))

        threading.Thread(target=worker, daemon=True).start()
        self.after(150, self._poll_batch_queue)

    def _poll_batch_queue(self):
        try:
            while True:
                msg = self._batch_queue.get_nowait()
                if msg[0] == "progress":
                    _, done, total = msg
                    self.batch_progress["maximum"] = total
                    self.batch_progress["value"] = done
                    self.batch_status.config(text=f"已处理 {done}/{total}")
                elif msg[0] == "done":
                    result = msg[1]
                    self._on_batch_done(result)
                    return
                elif msg[0] == "error":
                    messagebox.showerror("批量处理失败", msg[1])
                    self.run_batch_btn.config(state="normal")
                    return
        except queue.Empty:
            pass
        self.after(150, self._poll_batch_queue)

    def _on_batch_done(self, result: pd.DataFrame):
        self.run_batch_btn.config(state="normal")
        n_low = (result.get("置信度1", pd.Series(dtype=str)) == "低").sum()
        n_none = (result.get("_备注", pd.Series(dtype=str)) != "").sum()
        self.batch_status.config(
            text=f"完成！共 {len(result)} 条，低置信度 {n_low} 条，需人工核查 {n_none} 条")
        out_path = filedialog.asksaveasfilename(defaultextension=".xlsx",
                                                  filetypes=[("Excel files", "*.xlsx")])
        if out_path:
            result.to_excel(out_path, index=False)
            messagebox.showinfo("已保存", f"结果已保存至：\n{out_path}")
        self._refresh_status()

