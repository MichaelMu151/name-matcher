    def on_search_codebook(self):
        level = self.cb_level_filter.get()
        kw = self.cb_keyword.get().strip()
        df = self.matcher.codebook.search(level=level, keyword=kw)
        for i in self.cb_tree.get_children():
            self.cb_tree.delete(i)
        for _, r in df.iterrows():
            self.cb_tree.insert("", "end", values=(r["dept_id"], r["level"], r["standard_name"], r["aliases"]))

    def on_add_department(self):
        dept_id = self.new_dept_id.get().strip()
        level = self.new_level.get()
        std_name = self.new_std_name.get().strip()
        aliases = self.new_aliases.get().strip()
        if not dept_id or not std_name:
            messagebox.showwarning("提示", "dept_id 和标准名不能为空")
            return
        try:
            self.matcher.codebook.add_department(dept_id, level, std_name, aliases)
            self.matcher.codebook.save()
            self.matcher._cache.clear()
        except CodeBookError as e:
            messagebox.showerror("添加失败", str(e))
            return
        messagebox.showinfo("成功", f"已添加部门：{std_name}（{dept_id}, {level}）")
        self.on_search_codebook()
        self._refresh_status()

