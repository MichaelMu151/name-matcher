        ttk.Label(top, textvariable=self.batch_path).grid(row=0, column=1, sticky="w", padx=4)

        ttk.Label(top, text="返回候选数 Top-K：").grid(row=1, column=0, sticky="e", padx=4, pady=4)
        self.topk_var = tk.IntVar(value=3)
        ttk.Spinbox(top, from_=1, to=5, textvariable=self.topk_var, width=5).grid(row=1, column=1, sticky="w")

        self.batch_preview = ttk.Label(f, text="", foreground="#555")
        self.batch_preview.pack(anchor="w", padx=10)

