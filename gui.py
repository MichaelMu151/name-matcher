    def choose_train_file(self):
        path = filedialog.askopenfilename(filetypes=[("Excel files", "*.xlsx")])
        if path:
            self.train_path_var.set(path)

    def run_train(self):
        path = self.train_path_var.get()
        if not os.path.exists(path):
            messagebox.showerror("错误", f"找不到训练数据文件：{path}")
            return
        self.train_btn.config(state="disabled")
        self.train_status.config(text="训练中，请稍候...")

        def worker():
            try:
                metrics = self.matcher.train(
                    path, MODEL_PATH,
                    model_type=self.model_type_var.get(),
                    test_size=self.test_size_var.get(),
                    hard_negatives=self.hard_neg_var.get(),
                )
                self._train_queue.put(("done", metrics))
            except MatcherError as e:
                self._train_queue.put(("error", str(e)))
            except Exception as e:
                self._train_queue.put(("error", f"未知错误: {e}"))

        threading.Thread(target=worker, daemon=True).start()
        self.after(200, self._poll_train_queue)

    def _poll_train_queue(self):
        try:
            msg = self._train_queue.get_nowait()
        except queue.Empty:
            self.after(200, self._poll_train_queue)
            return

        self.train_btn.config(state="normal")
        if msg[0] == "error":
            self.train_status.config(text="训练失败")
            messagebox.showerror("训练失败", msg[1])
            return

        metrics = msg[1]
        top1 = metrics.get("top1_acc")
        top3 = metrics.get("top3_acc")
        if top1 is not None:
            summary = (f"训练完成！训练样本={metrics['n_train']}，验证样本={metrics['n_val']}，"
                       f"Top1准确率={top1:.1%}，Top3准确率={top3:.1%}")
        else:
            summary = f"训练完成！样本数={metrics['n_train']}（样本量过小，未做验证集评估）"
        self.train_status.config(text=summary)

        self.train_log.configure(state="normal")
        self.train_log.insert("end", f"[{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}] {summary}\n")
        self.train_log.see("end")
        self.train_log.configure(state="disabled")
        self._refresh_status()


